def add_complex_numbers(x1, y1, x2, y2):
    complex_number_one = complex(x1, y1)
    complex_number_two = complex(x2, y2)
    return complex_number_one + complex_number_two

def subtract_complex_numbers(x1, y1, x2, y2):
    complex_number_one = complex(x1, y1)
    complex_number_two = complex(x2, y2)
    return complex_number_one - complex_number_two

