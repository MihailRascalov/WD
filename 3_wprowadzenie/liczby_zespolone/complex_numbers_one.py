def get_real_value(x, y):
    """Function return the real part of a complex number
    An complex number is represented by “ x + yi"""
    value = complex(x, y)
    return value.real

def get_imag_value(x, y):
    """Function return the imaginary part of a complex number
    An complex number is represented by “ x + yi"""
    value = complex(x, y)
    return value.imag